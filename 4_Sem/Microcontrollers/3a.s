	AREA PROGRAM6,CODE,READONLY
N RN 1 ; NAMING REGISTER R1 AS N
RESULT RN 2 ; SAME AS ABOVE
EVEN_NUM RN 3
ENTRY
	MOV N,#5
	MOV RESULT,#0
	MOV EVEN_NUM,#2
	MOV R4,#0x40000000
LOOP
	ADD RESULT,RESULT,EVEN_NUM
	ADD EVEN_NUM,EVEN_NUM,#2
	SUBS N,N,#1
	STR RESULT,[R4],#4
	BNE LOOP
STOP B STOP
	END	
	
